public class dataType {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 10.5f;
        System.out.println(price);
        int value = 45;
        System.out.println(value);
        // "long" is used for saving the integers bot is very large vlaue
        // "double" is used for saving the float but of very large value
        short n = 345;
        System.out.println(n);

    }

}
