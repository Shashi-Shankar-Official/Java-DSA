public class typecasting {
    public static void main(String[] args) {
        /*
         * float a = 25.12f;
         * // int b = a; this will give an error
         * int b = (int) a;
         * System.out.println(b);
         */
        char ch = 'a';
        char ch2 = 'b';
        int number = ch;
        int number2 = ch2;
        System.out.println(number);
        System.out.println(number2);

    }

}
