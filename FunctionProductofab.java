public class FunctionProductofab {
    public static int multiply(int a, int b) {
        int product = a * b;
        return product;
    }

    public static void main(String[] args) {
        // int a = 3;
        // int b = 5;
        // int prod = multiply(a, b);
        int prod = multiply(3, 5);
        System.out.println("a*b=" + prod);
        prod = multiply(7, 9);
        System.out.println("a*b=" + prod);
        // benefit of function

    }

}
